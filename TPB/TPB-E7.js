function escribirNumero(numero) {
	if(numero == 1){
		return "Uno";
	}
	if(numero == 2){
		return "Dos";
	}
	if(numero == 3){
		return "Tres";
	}
	if(numero == 4){
		return "Cuatro";
	}
	if(numero == 5){
		return "Cinco";
	}
	if(numero == 6){
		return "Seis";
	}
	if(numero == 7){
		return "Siete";
	}
	if(numero == 8){
		return "Ocho";
	}
	if(numero == 9){
		return "Nueve";
	}
	if(numero == 10){
		return "Diez";
	}
}

console.log(escribirNumero(10));
console.log(escribirNumero(6));
console.log(escribirNumero(1));
console.log(escribirNumero(8));
console.log(escribirNumero(2));